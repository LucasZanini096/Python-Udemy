#Pascalcase: significa que todas as palavras iniciam com letra maiúscula e nada é usado para separá-las, comocem:
#MinhaClasse,Classe,MeuObjeto,MeuProgramaMuitoLegal. Esta convenção é utilizada para classes em Python;

#CamelCase - a única diferença de camelCase para PascalCase é a primeira letra. Em camelCase 
#a primeira letra sempre será minúscula e o restante das palavaras deverá inicar com letra maiúscula. Essa convenção não é utilizada em Python 

#Snake_case - este é o padrão usado em Python para definir qualquer coisa que não for uma classe. Todas as letras serão maíusculas e separadas por um underline.

